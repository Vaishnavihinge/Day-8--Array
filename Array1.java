
public class Array1 {

    public static void main(String[] args) {

        int arr[] = new int[3]; //Initialization of array

        arr[0] = 10; //Declaration
        arr[1] = 20;
        arr[2] = 30;

        System.out.println(arr[0]);  //Accessing elements
        System.out.println(arr[1]);
        System.out.println(arr[2]);

        System.out.println("Array length : " + arr.length); //Array length
    }
}
